def get_role_prompt(role_name: str) -> str:
    return GamePrompts.ROLE_PROMPTS.get(role_name, GamePrompts.SYSTEM_PROMPT)


def get_phase_prompt(phase: str) -> str:
    return GamePrompts.PHASE_PROMPTS.get(phase, "")


def get_spectator_prompt(state: dict) -> str:
    return GamePrompts.SPECTATOR_PROMPT.format(state=state)


def get_player_prompt(state: dict) -> str:
    return GamePrompts.PLAYER_PROMPT.format(state=state)
